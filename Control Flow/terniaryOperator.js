// Nullish Coalescing Operator (??) : null undefined

let val1 = 5 ?? 10;
console.log(val1); 

let val2 = null ?? 10;
console.log(val2);

let val3 = undefined ?? 10 ?? 20;
console.log(val3);

// Terniary Operator 

// condition ? true : false

const iceCreamRate = 40;

iceCreamRate > 50 ? console.log("Expensive") : console.log("Affordable");

